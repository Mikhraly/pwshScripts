"R1 = R4 = " + [System.Math]::Round($R1, 0) + " Ом";
"R3 = " + [System.Math]::Round($R3, 0) + " Ом";
"R2 = " + [System.Math]::Round($R2, 0) + " Ом";
"Cout = " + $CoutMin * 1e6 + " ... " + $CoutMax *1e6 + " мкФ";