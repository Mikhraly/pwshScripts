# Входные данные
$F = 700;
$C1 = $C2 = 0.000001;

# Расчет
$R1 = $R4 = 1 / (2 * [System.Math]::PI * $C1 * $F);
$R3 = 19 * $R1;
$R2 = $R1 / 19;
$CoutMin = 100 * $C1;
$CoutMax = 1000 * $C1;

# Вывод
